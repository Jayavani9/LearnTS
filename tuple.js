//A tuple is a typed array with a pre-defined length and types for each index.
var tup;
tup = [5, true, 'Tuple Array'];
//read only tuple
var tupr;
tupr = [6, false, 'read only tuple'];
console.log(tupr);
console.log(typeof tupr);
//named tuple
var gr = [55.2, 41.3];
console.log(gr);
console.log(typeof gr);
